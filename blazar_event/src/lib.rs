//! Definition of several types of events.

/// Kinds of events.
#[derive(Debug)]
pub enum Event {
    // Window
    Close,
    GainFocus,
    LoseFocus,
    Resize { width: u32, height: u32 },
    // Keyboard
    KeyPress { key: Key },
    KeyRelease { key: Key },
    // Mouse
    MouseButtonPress { button: Button, x: i32, y: i32 },
    MouseButtonRelease { button: Button, x: i32, y: i32 },
    MouseScrollUp,
    MouseScrollDown,
    MouseMove { x: i32, y: i32 },
}

